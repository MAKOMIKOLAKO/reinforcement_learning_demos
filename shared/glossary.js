// ---------------------------------------------------------------------------
// Extension API
// ---------------------------------------------------------------------------

/**
 * Merges page-specific terms into the shared glossary map without requiring
 * edits to this file. Overwrites any existing key with the same name.
 *
 * @param {Object<string,string>} newTerms - Map of term -> definition to add.
 */
export function addTerms(newTerms) {
  Object.assign(glossary, newTerms);
}

// ---------------------------------------------------------------------------
// Markup generation
// ---------------------------------------------------------------------------

let idCounter = 0;

/** Escapes text for safe interpolation into HTML attribute or text content. */
function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

/** Builds a stable-ish unique id for a tooltip element on this page load. */
function nextId() {
  idCounter += 1;
  return `glossary-tip-${idCounter}`;
}

/**
 * Returns HTML markup for an inline glossary term: a focusable button
 * displaying `displayText` (or the term itself), plus a hidden tooltip
 * <span> with the definition, wired via ARIA attributes. Call initGlossary()
 * once after this markup is inserted into the DOM so the toggle/dismiss
 * behavior is attached.
 *
 * If the term isn't found in the glossary map, the text is still rendered,
 * but plainly (no button/tooltip), so a typo doesn't break the page.
 *
 * @param {string} term - Key to look up in the glossary map (case-insensitive).
 * @param {string} [displayText] - Text to show instead of the term itself.
 * @returns {string} HTML markup string.
 */
export function glossaryTerm(term, displayText) {
  const key = Object.keys(glossary).find(
    (candidate) => candidate.toLowerCase() === String(term).toLowerCase()
  );
  const shown = escapeHtml(displayText || term);

  if (!key) {
    return `<span class="glossary-missing">${shown}</span>`;
  }

  const definition = escapeHtml(glossary[key]);
  const tipId = nextId();

  return (
    `<span class="glossary-term-wrap">` +
    `<button type="button" class="glossary-term" data-glossary-toggle ` +
    `aria-describedby="${tipId}" aria-expanded="false">${shown}</button>` +
    `<span id="${tipId}" class="glossary-tooltip" role="tooltip" hidden>${definition}</span>` +
    `</span>`
  );
}

// ---------------------------------------------------------------------------
// Interaction wiring
// ---------------------------------------------------------------------------

const WIRED_FLAG = "__rlGlossaryWired";

/**
 * Wires up click/keyboard toggle and dismiss behavior for every glossary term
 * currently in `root` (defaults to the whole document). Safe to call more
 * than once — a page can call it again after inserting more glossary markup;
 * already-wired root/document combinations are skipped via a flag.
 *
 * Behavior:
 *  - Click or Enter/Space on a glossary-term button toggles its tooltip.
 *  - Escape closes any open tooltip and returns focus to its button.
 *  - Clicking/tapping outside an open tooltip closes it.
 *  - Only one tooltip is open at a time.
 *
 * @param {Document|Element} [root=document] - Scope to search for glossary
 *   term buttons that haven't been wired yet.
 */
export function initGlossary(root = document) {
  const buttons = root.querySelectorAll(
    "[data-glossary-toggle]:not([data-glossary-wired])"
  );

  buttons.forEach((button) => {
    button.setAttribute("data-glossary-wired", "true");
    button.addEventListener("click", (event) => {
      event.stopPropagation();
      toggleTooltip(button);
    });
  });

  if (!document[WIRED_FLAG]) {
    document[WIRED_FLAG] = true;

    document.addEventListener("keydown", (event) => {
      if (event.key === "Escape") {
        const openButton = document.querySelector(
          '[data-glossary-toggle][aria-expanded="true"]'
        );
        if (openButton) {
          closeTooltip(openButton);
          openButton.focus();
        }
      }
    });

    document.addEventListener("click", () => {
      closeAllTooltips();
    });
  }
}

function getTooltip(button) {
  const tipId = button.getAttribute("aria-describedby");
  return tipId ? document.getElementById(tipId) : null;
}

function toggleTooltip(button) {
  const isOpen = button.getAttribute("aria-expanded") === "true";
  closeAllTooltips();
  if (!isOpen) {
    openTooltip(button);
  }
}

function openTooltip(button) {
  const tooltip = getTooltip(button);
  if (!tooltip) return;
  tooltip.hidden = false;
  button.setAttribute("aria-expanded", "true");
}

function closeTooltip(button) {
  const tooltip = getTooltip(button);
  if (!tooltip) return;
  tooltip.hidden = true;
  button.setAttribute("aria-expanded", "false");
}

function closeAllTooltips() {
  document
    .querySelectorAll('[data-glossary-toggle][aria-expanded="true"]')
    .forEach((button) => closeTooltip(button));
}
