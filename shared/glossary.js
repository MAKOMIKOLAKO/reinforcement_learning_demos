/**
 * shared/glossary.js
 *
 * Dependency-free hover/click/focus tooltip glossary for the RL Demos site.
 * Works with mouse, touch, and keyboard: terms are rendered as a <button>
 * (focusable, tabindex-native) that toggles a tooltip on click or Enter/Space,
 * and dismisses on Escape or an outside click/tap. ARIA attributes describe
 * the relationship for screen readers.
 *
 * Exposed API:
 *   glossary                -> the mutable term -> definition map (Object)
 *   addTerms(newTerms)      -> merges newTerms into the shared glossary map
 *   glossaryTerm(term, displayText) -> string (HTML markup for a glossary span)
 *   initGlossary(root)      -> void (wires up event listeners once per page;
 *                                    root defaults to document)
 *
 * Usage on a demo page:
 *   import { glossaryTerm, addTerms, initGlossary } from '../shared/glossary.js';
 *   addTerms({ 'reward shaping': 'Modifying rewards to speed up learning.' });
 *   container.innerHTML = `Uses ${glossaryTerm('replay buffer')} to store...`;
 *   initGlossary(); // call once, after the markup above is in the DOM
 */

